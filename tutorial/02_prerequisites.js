/**
 * 1. Install <a href="https://www.npmjs.com/">NPM</a>.
 * 2. Run `npm install protoboard`.
 * 3. Install <a href="http://gulpjs.com/">Gulp</a>: `npm install gulp`.
 * 4. Protoboard relies on <a href="http://www.html5rocks.com/en/tutorials/webcomponents/imports/">
 *    HTML Import</a>. So you will need an HTTP server serving static files. A simple one you can
 *    use is <a href="https://www.npmjs.com/package/http-server">http-server</a>:
 *    `npm install http-server`.
 * 5. Navigate to the root directory of the project (where the `gulpfile.js`) file is, and run
 *    `gulp compile`. This will compile all the source files.
 * 6. Run `http-server` from the root directory of the project, and navigate to the file you want to
 *    open. A good one to start is `ex/all/index.html`.
 *
 * @class 2 Prerequisites
 * @module tutorial
 */